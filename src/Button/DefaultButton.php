namespace Popov\ZfcDataGridPlugin\Button;

abstract class DefaultButton
{
    protected $caption = "NewButton";

    //protected $icon = "ui-icon-newwin";
    protected $icon = "glyphicon-file";

    protected $position = 100;

    protected $title = "";

    protected $cursor = "pointer";

    protected $id = null;

    protected $name;

    protected $options = [];

    /**
     * @return mixed
     */
    public function getCaption()
    {
        return $this->caption;
    }

    /**
     * @param mixed $caption
     */
    public function setCaption($caption): void
    {
        $this->caption = $caption;
    }

    /**
     * @return string
     */
    public function getIcon(): string
    {
        return $this->icon;
    }

    /**
     * @param string $icon
     */
    public function setIcon(string $icon): void
    {
        $this->icon = $icon;
    }

    /**
     * @return mixed
     */
    public function getPosition()
    {
        return $this->position;
    }

    /**
     * @param mixed $position
     */
    public function setPosition($position): void
    {
        $this->position = $position;
    }

    /**
     * @return mixed
     */
    public function getTitle()
    {
        return $this->title;
    }

    /**
     * @param mixed $title
     */
    public function setTitle($title): void
    {
        $this->title = $title;
    }

    /**
     * @return mixed
     */
    public function getCursor()
    {
        return $this->cursor;
    }

    /**
     * @param mixed $cursor
     */
    public function setCursor($cursor): void
    {
        $this->cursor = $cursor;
    }

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id): void
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        $class = get_class($this);
        $className = explode('\\', $class);
        $name = end($className);
        $name = lcfirst(str_replace('Button', '', $name));
        $this->name = $name;

        return $this->name;
    }

    /**
     * @return array
     */
    public function getOptions(): array
    {
        return $this->options;
    }

    /**
     * @param array $options
     * @return DefaultButton
     */
    public function setOptions(array $options): self
    {
        $this->options = $options;

        return $this;
    }
}